package main.java11;

public class StringEnhancements {

	public static void main(String[] args) {
		
		// isBlank
		System.out.println("Test".isBlank());
		
		// lines
		System.out.println("as".lines());
		
		// repeat
		System.out.println("Test".repeat(2));
		
		// strip
		System.out.println("  sad   ".strip());
		
	}
	
}
